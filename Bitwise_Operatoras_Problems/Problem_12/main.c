#include <stdio.h>

/*************************************************************************
                       Macros Section

*************************************************************************/
#define INT_SIZE  ((sizeof(int))*(8))


/*************************************************************************
                       Global Variables Section

*************************************************************************/


/*************************************************************************
                       Function Prototypes Section

*************************************************************************/



/*************************************************************************
                       Main Function Section
*************************************************************************/

int main()
{
    int Number ;
    int iterator, Zeros_Counter = 0, Ones_Counter = 0 ;

    /* Input number from user */
    printf("Enter any number: ");
    scanf("%d", &Number);

    for(iterator = 0 ; iterator < INT_SIZE ; iterator++)
    {
        if((Number >> iterator) & 1)
        {
            Ones_Counter++;
        }
        else
        {
            Zeros_Counter++;
        }

    }
    printf("Total zero bit is %d\n", Zeros_Counter);
    printf("Total one bit is %d", Ones_Counter);

    return 0;
}

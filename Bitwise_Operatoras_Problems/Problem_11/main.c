int main()
{
    int num, flippedNumber;

    /* Input number from user */
    printf("Enter any number: ");
    scanf("%d", &num);

    flippedNumber = ~num;

    printf("Original number = %d\n", num);
    printf("Number after bits are flipped = %d", flippedNumber);

    return 0;
}

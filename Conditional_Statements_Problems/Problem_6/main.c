void Check_Leap_Year(int Year)
{
    if(((Year % 4 == 0) && (Year % 100 != 0)) || (Year % 400 == 0))
    {
        printf("Year %d is a Leap Year\n",Year);
    }
    else
        printf("Year %d is't a Leap Year\n",Year);
}

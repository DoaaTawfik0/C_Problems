/*************************************************************************
                       Functions Definition Section
*************************************************************************/

void Number_Status(int Number)
{
    if(Number > 0)
    {
        printf("Number %d is Positive\n",Number);
    }
    else if(Number < 0)
    {
        printf("Number %d is Negative\n",Number);
    }
    else
        printf("It's a Zero Number\n");
}

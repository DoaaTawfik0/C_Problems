void Check_Days_Number(int Month)
{
    if((Month >= 1) && (Month <= 12))
    {
        if((Month == 4) || (Month == 6) || (Month == 9) || (Month == 11))
        {
           printf("30 days");
        }
        else if(Month == 2)
        {
           printf("28/29 days");
        }
        else
        {
            printf("31 days");
        }
    }
    else
        printf("Invalid input! Please enter month number between (1-12).");

}

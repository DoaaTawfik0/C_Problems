void Check_Alphabet(char Character)
{
    if(((Character >= 'a') && (Character <= 'z')) || ((Character >= 'A') && (Character <= 'Z')))
    {
        printf("Character %c is an Alphabet\n",Character);
    }
    else
        printf("Character %c is't an Alphabet\n",Character);
}

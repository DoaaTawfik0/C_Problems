int main()
{
    int Number1, Number2, Res;

    /*Input 2 Numbers From User*/
    printf("Enter First Number: ");
    scanf("%d",&Number1);
    printf("Enter Second Number: ");
    scanf("%d",&Number2);

    /*Finding Max Number*/
    Res = Max_Number(Number1, Number2);

    /*Printing Max Number*/
    printf("Maximum Number = %d\n",Res);

    return 0;
}

/*************************************************************************
                       Functions Definition Section
*************************************************************************/

int Max_Number(int Num1, int Num2)
{
    if(Num1 >= Num2)
        return Num1 ;
    else if (Num2 >= Num1)
        return Num2;


}
